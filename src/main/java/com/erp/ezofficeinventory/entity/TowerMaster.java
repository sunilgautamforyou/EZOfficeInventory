package com.erp.ezofficeinventory.entity;

public class TowerMaster {
	private int towerId;
	private String towerNumber;
	private String towerDescription;
	public int getTowerId() {
		return towerId;
	}
	public void setTowerId(int towerId) {
		this.towerId = towerId;
	}
	public String getTowerNumber() {
		return towerNumber;
	}
	public void setTowerNumber(String towerNumber) {
		this.towerNumber = towerNumber;
	}
	public String getTowerDescription() {
		return towerDescription;
	}
	public void setTowerDescription(String towerDescription) {
		this.towerDescription = towerDescription;
	}
	
	
}
